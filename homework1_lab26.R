  theme(axis.title = element_text(size = 18), legend.text = element_text(size = 18)) + guides(colour = guide_legend(override.aes = list(size = 10)))
ggsave(filename = "tutorial2.jpg", height = 7, width = 12, plot = plot, quality = 50)
